public class Controller
{
    private string left = "left";
    private string right = "right";

    //Getters
    public string GetLeft()
    {
        return left;
    }

    public string GetRight()
    {
        return right;
    }

    //Setters
    public void SetLeft(string left)
    {
        this.left = left;
    }

    public void SetRight(string right)
    {
        this.right = right;
    }
}
